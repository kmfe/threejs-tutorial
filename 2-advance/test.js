const formatLZData = data => {
  const newData = {
    total: data.total,
    list: []
  }
  let k = 0
  let i = 0
  for (i; i < data.list.length; i++) {
    const arr = []
    arr.push({
      drawItem: data.list[i][0],
      drawCode: data.list[i][1],
      drawType: data.list[i][2]
    })
    k = i + 1
    for (k; k < data.list.length; k++) {
      if (data.list[k][2] === data.list[i][2]) {
        arr.push({
          drawItem: data.list[k][0],
          drawCode: data.list[k][1],
          drawType: data.list[k][2]
        })
        i = k
      } else {
        break
      }
    }
    newData.list.push(arr)
  }
  return newData
}

